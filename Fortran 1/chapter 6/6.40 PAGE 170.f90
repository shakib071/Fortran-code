DIMENSION A(3,3)
DO 100 I=1,3
    DO 200 J=1,3
        READ*,A(I,J)
200 CONTINUE
100 CONTINUE
SUM=0.0
SUM1=0.0
DO 300 I=1,3
    DO 400 J=1,3
        IF(J.GT.I)SUM=SUM+A(I,J)
        IF(I.GT.J)SUM1=SUM1+A(I,J)
400 continue
300 CONTINUE
PRINT*,'THE SUM OF ELEMENT ABOVE MAIN DIAGNAL IS',SUM
PRINT*,'THE SUM OF ELEMENT BELOW MAIN DIAGNAL IS',SUM1
STOP
END
