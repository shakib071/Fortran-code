DIMENSION SCORE(35)
DO 100 I=1,35
    READ*,SCORE(I)
100 CONTINUE
TOTAL=0.0
DO 200 J=1,35
    TOTAL=TOTAL+SCORE(J)
200 CONTINUE
AVE=TOTAL/35.0
M=0
N=0
DO 300 K=1,35
   IF(SCORE(K).LE.60) M=M+1
    IF(SCORE(K).EQ.100) N=N+1
300 CONTINUE
PRINT*,'AVE SCORE',AVE,'NUMBER OF STUDENT FAILED IS',M,'PERFECT PAPER',N
STOP
END
