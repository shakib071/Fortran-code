DIMENSION A(10),DA(9),D2A(8),D3A(7)
DO 20 I=1,10
    READ*,A(I)
20 CONTINUE
DO 100 K=1,9
    DA(K)=A(K+1)-A(K)
100 CONTINUE
DO 200 J=1,8
    D2A(J)=DA(J+1)-DA(J)
200 CONTINUE
DO 300 L=1,7
    D3A(L)=D2A(L+1)-D2A(L)
300 CONTINUE
WRITE(6,11),A
11 FORMAT(1X,10(2X,F8.2))
WRITE(6,12),DA
12 FORMAT(3X,10(2X,F8.2))
WRITE(6,13),D2A
13 FORMAT(5X,10(2X,F8.2))
WRITE(6,14),D3A
14 FORMAT(7X,10(2X,F8.2))
STOP
END
