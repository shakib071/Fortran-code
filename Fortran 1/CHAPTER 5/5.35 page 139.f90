!SUM OF 1ST 1000 TERM
SUM=0.0
DO 100 I=1,1000
    P=FLOAT(I)
    SUM=SUM+(1.0/(P**2))
100 CONTINUE
PI=SQRT(SUM*6.0)
PRINT*,'SUM OF 1ST 1000 TERM /PI VALUE IS',PI

!SUM OF 1ST 1000 TERM IN REVERSE ORDER
SUM1=0.0
DO 200 J=1,1000
    K=1001-J
    Q=FLOAT(K)
    SUM1=SUM1+(1.0/(Q**2))
200 CONTINUE
PI1=SQRT(SUM1*6.0)
PRINT*,'SUM OF 1ST 1000 TERM IN REVERSE ORDER /PI VALUE IS',PI1
!DIFFERENCE
DIF=ABS(PI1-PI)
WRITE(6,11),DIF
11 FORMAT('THE DIFFERENCE IS',1X,F10.8)
STOP
END
